import 'package:flutter/material.dart';
import 'package:flutter_screenutil/flutter_screenutil.dart';
import 'package:food_delivery_app/common/app_style.dart';
import 'package:food_delivery_app/common/reusable_text.dart';
import 'package:food_delivery_app/constants/constants.dart';
import 'package:food_delivery_app/views/category/category_page.dart';
import 'package:get/get.dart';

class CategoryTile extends StatelessWidget {
  CategoryTile({
    super.key,
    required this.category,
  });

  var category;

  @override
  Widget build(BuildContext context) {
    return ListTile(
      onTap: (){
        Get.to(
        () => const CategoryPage(),
        transition: Transition.zoom,
        duration: const Duration(milliseconds: 500),
      );
      },
      leading: CircleAvatar(
        radius: 20.r,
        backgroundColor: kGrayLight,
        child: Image.asset(category['imageUrl'], fit: BoxFit.contain,),
      ),
      title: ReusableText(text: category['title'], style: appStyle(15, kGray, FontWeight.normal)),
      trailing: const Icon(Icons.arrow_forward_ios, size: 15,),
    );
  }
}
